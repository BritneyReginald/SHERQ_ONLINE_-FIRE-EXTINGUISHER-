def get_unit_and_category(qr_id):
    unit = Extinguisher.query.filter_by(qr_code_id=qr_id).first()
    category = 'Fire Extinguisher'
    if not unit:
        unit = HoseReel.query.filter_by(qr_code_id=qr_id).first()
        category = 'Fire Hose Reel'
    if not unit:
        unit = Hydrant.query.filter_by(qr_code_id=qr_id).first()
        category = 'Fire Hydrant'
    return unit, category

