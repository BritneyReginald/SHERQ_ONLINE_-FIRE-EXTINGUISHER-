@app.route('/admin/add_asset/<category>', methods=['GET', 'POST'])
def add_asset(category):
    if not session.get('admin_logged_in'):
        return redirect(url_for('login'))
    if category in ['extinguisher', 'extinguishers', 'Fire Extinguisher']:
        next_num = Extinguisher.query.count() + 1
        generated_id = f"EQ-{next_num:03d}"
    elif category in ['hose_reel', 'hose', 'Fire Hose Reel']:
        next_num = HoseReel.query.count() + 1
        generated_id = f"HSR-{next_num:03d}"
    else:
        next_num = Hydrant.query.count() + 1
        generated_id = f"HYD-{next_num:03d}"
    if request.method == 'POST':
        form_location = request.form.get('location')
        form_weight = request.form.get('weight') or request.form.get('unit_weight')
        if category in ['extinguisher', 'extinguishers', 'Fire Extinguisher']:
            new_asset = Extinguisher(qr_code_id=generated_id, extinguisher_type=request.form.get('type', 'ABC Powder'), unit_weight=form_weight, authorized_location=form_location, status='In Position')
        elif category in ['hose_reel', 'hose', 'Fire Hose Reel']:
            new_asset = HoseReel(qr_code_id=generated_id, unit_weight=form_weight or 'Standard 30m', authorized_location=form_location, status='In Position')
        else:
            new_asset = Hydrant(qr_code_id=generated_id, unit_weight=form_weight or 'Standard Flow', authorized_location=form_location, status='In Position')
        db.session.add(new_asset)
        db.session.commit()
        flash(f"Asset {generated_id} added successfully!")
        return redirect(url_for('register'))
    return render_template('add_asset.html', category=category, next_id=generated_id)

